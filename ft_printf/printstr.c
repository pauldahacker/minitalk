#include "ft_printf.h"

int	printstr(char *s)
{
	int	i;

	if (!s)
		return (write(1, "(null)", 6));
	i = 0;
	while (s[i])
	{
		if (write(1, &(s[i]), 1) == -1)
			return (-1);
		++i;
	}
	return (i);
}
